  version "0.652.0.6520764,06fc92e9ee084959"
  sha256 arm:   "5b8e3985b5e81431a7f1026ecb78e7054b9afc30cff3b9bf4368ce7022b33ad3",
         intel: "abc201510b244dd437dd856c14497d836bb038466424eae0e0bffc4a8cb07d3f"

