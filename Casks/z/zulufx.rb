         intel: "0d0ddb398d25f212cf84d8fdfca016c7172e2d4ff051c1886096577139a3ecad"

  url "https://cdn.azul.com/zulu/bin/zulu#{version.csv.second}-ca-fx-jdk#{version.csv.first}-macosx_#{arch}.dmg",
