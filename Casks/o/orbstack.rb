cask "orbstack" do
  arch arm: "arm64", intel: "amd64"

  version "1.4.3_16673"
  sha256 arm:   "afcb82173d28733b3f693c9d8eac794d644734610ad8ba4ce7fe618827133759",
         intel: "48a570d7108c0d19c849e86932d7967dfb16fb060b07d065c1e79c1da9852498"

