cask "visual-studio-code@insiders" do
  arch arm: "-arm64"

  version "1.105.0-insider,1758776617323,afe5914687af530d501e7fa9000253ebf76ce9ed"
  sha256 arm:   "9e81cacfeb6b4ffdc2bd577169f24a9a8eee1ebde7ec3433b940083277c13b8b",
