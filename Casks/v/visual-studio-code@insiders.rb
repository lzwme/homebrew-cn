  arch arm: "-arm64"

  version "1.120.0-insider,1778173544165,ca2ded47a87743fae81319a26b68f33cf1043998"
  sha256 arm:   "4ba7358d23005313f75bf5b3e5166239c8559587dde3c31ade76c129482e6853",
