cask "miniforge" do
  arch arm: "arm64", intel: "x86_64"

  version "24.3.0-0"
  sha256 arm:   "9b3c3d9fa30437592e680390f2b27d45c5d5cfcbfad9a1af233f70a6d8be71a1",
         intel: "26a80621b146d60e5ae0d896b83ec138416653b951286361b1f93a804cb6a8d9"

  url "https:github.comconda-forgeminiforgereleasesdownload#{version}Miniforge3-#{version}-MacOSX-#{arch}.sh"
  name "miniforge"
  desc "Minimal installer for conda specific to conda-forge"
  homepage "https:github.comconda-forgeminiforge"

  livecheck do
    url :homepage
    regex(v?(\d+(?:[.-]\d+)+)i)
    strategy :github_latest
  end

  auto_updates true
  conflicts_with cask: [
    "mambaforge",
    "miniconda",
  ]
  container type: :naked

  installer script: {
    executable: "Miniforge3-#{version}-MacOSX-#{arch}.sh",
    args:       ["-b", "-p", "#{caskroom_path}base"],
  }
  binary "#{caskroom_path}basecondabinconda"
  binary "#{caskroom_path}basecondabinmamba"

  uninstall delete: "#{caskroom_path}base"

  zap trash: [
    "~.conda",
    "~.condarc",
  ]

  caveats <<~EOS
    Please run the following to setup your shell:
      conda init "$(basename "${SHELL}")"
  EOS
end