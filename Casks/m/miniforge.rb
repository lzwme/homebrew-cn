cask "miniforge" do
  arch arm: "arm64", intel: "x86_64"

  version "24.1.2-0"
  sha256 arm:   "b372d2189e5752e92a4b4bbc4a91b8a08a4f0612d39c0762111c8dd60914b1c7",
         intel: "e447e7e0173e94ea8725a4c6675c29bcd3082f8fff7948b47e05af72b12d1261"

  url "https:github.comconda-forgeminiforgereleasesdownload#{version}Miniforge3-#{version}-MacOSX-#{arch}.sh"
  name "miniforge"
  desc "Minimal installer for conda specific to conda-forge"
  homepage "https:github.comconda-forgeminiforge"

  livecheck do
    url :homepage
    regex(v?(\d+(?:[.-]\d+)+)i)
    strategy :github_latest
  end

  auto_updates true
  conflicts_with cask: "miniconda"
