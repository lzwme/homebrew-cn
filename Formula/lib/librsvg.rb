class Librsvg < Formula
  desc "Library to render SVG files using Cairo"
  homepage "https:wiki.gnome.orgProjectsLibRsvg"
  url "https:download.gnome.orgsourceslibrsvg2.57librsvg-2.57.2.tar.xz"
  sha256 "9c2b73947924d3f0f5653436c3ee3b170128930ab6abd2303bb6d727b88d6471"
  license "LGPL-2.1-or-later"

  # librsvg doesn't use GNOME's "even-numbered minor is stable" version scheme.
  # This regex matches any version that doesn't have a 90+ patch version, as
  # those are development releases.
  livecheck do
    url :stable
    regex(librsvg[._-]v?(\d+\.\d+\.(?:\d|[1-8]\d+)(?:\.\d+)*)\.ti)
  end

  bottle do
    sha256                               arm64_sonoma:   "d758ebff6d6a85f6066461cb7bd35f8314e76d92c13ff8dd5f6bf8b59b9d96a1"
    sha256                               arm64_ventura:  "00b73fcdcc3191e20b3934fdb33c39ab71d736b4fb1f1c38f2ff9d5525dc5990"
    sha256                               arm64_monterey: "52add2f8a00c4002414ed178e8fdf3d537c8a51a7349188812f3fa6a317b63ef"
    sha256                               sonoma:         "0dbab45efbd8349b6579443f00cc5efe7cdf38f222ae6710037d9addddd54409"
    sha256                               ventura:        "4002c714774b36faf510847d15b2eace047b89be8da1d1461abfdbb7d9a6696f"
    sha256                               monterey:       "cd02ac2932b777d971368b4b4054a3f2ebada8759ab42d86c409d86f08fd5b47"
    sha256 cellar: :any_skip_relocation, x86_64_linux:   "387ae1240bad68e9e41fd944719feff6b44f93eb86ac535d8ada73aa621faa3c"
  end

  depends_on "gobject-introspection" => :build
  depends_on "pkg-config" => :build
  depends_on "rust" => :build
  depends_on "cairo"
  depends_on "gdk-pixbuf"
  depends_on "glib"
  depends_on "pango"

  def install
    args = %W[
      --disable-dependency-tracking
      --prefix=#{prefix}
      --disable-Bsymbolic
      --enable-tools=yes
      --enable-pixbuf-loader=yes
      --enable-introspection=yes
    ]

    system ".configure", *args

    # disable updating gdk-pixbuf cache, we will do this manually in post_install
    # https:github.comHomebrewhomebrewissues40833
    inreplace "gdk-pixbuf-loaderMakefile",
              "$(GDK_PIXBUF_QUERYLOADERS) > $(DESTDIR)$(gdk_pixbuf_cache_file) ;",
              ""

    system "make", "install",
      "gdk_pixbuf_binarydir=#{lib}gdk-pixbuf-2.02.10.0loaders",
      "gdk_pixbuf_moduledir=#{lib}gdk-pixbuf-2.02.10.0loaders"
  end

  def post_install
    # librsvg is not aware GDK_PIXBUF_MODULEDIR must be set
    # set GDK_PIXBUF_MODULEDIR and update loader cache
    ENV["GDK_PIXBUF_MODULEDIR"] = "#{HOMEBREW_PREFIX}libgdk-pixbuf-2.02.10.0loaders"
    system "#{Formula["gdk-pixbuf"].opt_bin}gdk-pixbuf-query-loaders", "--update-cache"
  end

  test do
    (testpath"test.c").write <<~EOS
      #include <librsvgrsvg.h>

      int main(int argc, char *argv[]) {
        RsvgHandle *handle = rsvg_handle_new();
        return 0;
      }
    EOS
    cairo = Formula["cairo"]
    fontconfig = Formula["fontconfig"]
    freetype = Formula["freetype"]
    gdk_pixbuf = Formula["gdk-pixbuf"]
    gettext = Formula["gettext"]
    glib = Formula["glib"]
    libpng = Formula["libpng"]
    pixman = Formula["pixman"]
    flags = %W[
      -I#{cairo.opt_include}cairo
      -I#{fontconfig.opt_include}
      -I#{freetype.opt_include}freetype2
      -I#{gdk_pixbuf.opt_include}gdk-pixbuf-2.0
      -I#{gettext.opt_include}
      -I#{glib.opt_include}glib-2.0
      -I#{glib.opt_lib}glib-2.0include
      -I#{include}librsvg-2.0
      -I#{libpng.opt_include}libpng16
      -I#{pixman.opt_include}pixman-1
      -D_REENTRANT
      -L#{cairo.opt_lib}
      -L#{gdk_pixbuf.opt_lib}
      -L#{gettext.opt_lib}
      -L#{glib.opt_lib}
      -L#{lib}
      -lcairo
      -lgdk_pixbuf-2.0
      -lgio-2.0
      -lglib-2.0
      -lgobject-2.0
      -lm
      -lrsvg-2
    ]
    flags << "-lintl" if OS.mac?
    system ENV.cc, "test.c", "-o", "test", *flags
    system ".test"
  end
end