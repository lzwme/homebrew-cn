class Allure < Formula
  desc "Flexible lightweight test report tool"
  homepage "https:github.comallure-frameworkallure2"
  url "https:repo.maven.apache.orgmaven2ioqametaallureallure-commandline2.34.0allure-commandline-2.34.0.zip"
  sha256 "b0554db20cc0472cba2474733560a89805bfaa29c81f65b66763af2ecf80436c"
  license "Apache-2.0"

  livecheck do
    url "https:search.maven.orgremotecontent?filepath=ioqametaallureallure-commandlinemaven-metadata.xml"
    regex(%r{<version>v?(\d+(?:\.\d+)+)<version>}i)
  end

  bottle do
    sha256 cellar: :any_skip_relocation, all: "5a1d8d7db1fcd72d2efb93abb3c38a2537f8c7f3ac5badfe62d65923c4aae04f"
  end

  depends_on "openjdk"

  def install
    # Remove all windows files
    rm(Dir["bin*.bat"])

    libexec.install Dir["*"]
    bin.install Dir["#{libexec}bin*"]
    bin.env_script_all_files libexec"bin", JAVA_HOME: Formula["openjdk"].opt_prefix
  end

  test do
    (testpath"allure-resultsallure-result.json").write <<~JSON
      {
        "uuid": "allure",
        "name": "testReportGeneration",
        "fullName": "org.homebrew.AllureFormula.testReportGeneration",
        "status": "passed",
        "stage": "finished",
        "start": 1494857300486,
        "stop": 1494857300492,
        "labels": [
          {
            "name": "package",
            "value": "org.homebrew"
          },
          {
            "name": "testClass",
            "value": "AllureFormula"
          },
          {
            "name": "testMethod",
            "value": "testReportGeneration"
          }
        ]
      }
    JSON
    system bin"allure", "generate", "#{testpath}allure-results", "-o", "#{testpath}allure-report"
  end
end