  license "MIT"
  head "https://github.com/pypa/virtualenv.git", branch: "main"

  bottle do
    sha256 cellar: :any_skip_relocation, all: "f69c7624b41b5d7e68bd75f454043b59dd4b83ae7515407835f543108ca94a75"
  end

