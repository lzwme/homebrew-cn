class V8 < Formula
  desc "Google's JavaScript engine"
  homepage "https:v8.devdocs"
  # Track V8 version from Chrome stable: https:chromiumdash.appspot.comreleases?platform=Mac
  url "https:github.comv8v8archiverefstags11.7.439.16.tar.gz"
  sha256 "df073b032cafab5124a166771b5d66f6afaa8412c4d5863d2ba8d52c253ced33"
  license "BSD-3-Clause"

  livecheck do
    url "https:chromiumdash.appspot.comfetch_releases?channel=Stable&platform=Mac"
    regex((\d+\.\d+\.\d+\.\d+)i)
    strategy :json do |json, regex|
      # Find the v8 commit hash for the newest Chromium release version
      v8_hash = json.max_by { |item| Version.new(item["version"]) }.dig("hashes", "v8")
      next if v8_hash.blank?

      # Check the v8 commit page for version text
      v8_page = Homebrew::Livecheck::Strategy.page_content(
        "https:chromium.googlesource.comv8v8.git+#{v8_hash}",
      )
      v8_page[:content]&.scan(regex)&.map { |match| match[0] }
    end
  end

  bottle do
    sha256 cellar: :any,                 arm64_sonoma:   "914f44e5948dadbda17c95651a6c690a0bca7b888d2b5c664595de5fcb8b7b77"
    sha256 cellar: :any,                 arm64_ventura:  "923dcb56245d63d810769af204979ce8ec79b8c5948129018ec3ca1c24a9b3b1"
    sha256 cellar: :any,                 arm64_monterey: "ed26e75302ce842f8d1db3f8deca7edc47da2bdbd86593fa8adc26e9e4efe9ab"
    sha256 cellar: :any,                 arm64_big_sur:  "bf3964edddd790884e2e230a7df0732e11e6a991f27dbdefb4cafdb759718e8d"
    sha256 cellar: :any,                 sonoma:         "bfb6eca5b46a983a19094d1ef7b2dbf984bfff076cb68157eaf00e0a3ce14424"
    sha256 cellar: :any,                 ventura:        "df68e49baa4c80fd8a0ac350d610b0b273960843b42c4276947ddd826a0b6bdf"
    sha256 cellar: :any,                 monterey:       "ac0f7843d7a98dd6ff7df3bb7d475999b124bb1d84ac8f0ae50eba85599330a4"
    sha256 cellar: :any,                 big_sur:        "e347443e3a34f8b43abd15e72ec214b4d277b65e7420bc8ff8f270556ff88e41"
    sha256 cellar: :any_skip_relocation, x86_64_linux:   "1b1e4ffb2d4b2c2e150bf969ec6e373130ca94a33ffeac66d301ac9111978e1e"
  end

  depends_on "ninja" => :build
  depends_on "python@3.11" => :build

  on_macos do
    depends_on "llvm" => :build
    depends_on xcode: ["10.0", :build] # required by v8
  end

  on_linux do
    depends_on "pkg-config" => :build
    depends_on "glib"
  end

  fails_with gcc: "5"

  # Look up the correct resource revisions in the DEP file of the specific releases tag
  # e.g. for CIPD dependency gn: https:chromium.googlesource.comv8v8.git+refstags<version>DEPS#64
  resource "gn" do
    url "https:gn.googlesource.comgn.git",
        revision: "811d332bd90551342c5cbd39e133aa276022d7f8"
  end

  resource "v8basetrace_eventcommon" do
    url "https:chromium.googlesource.comchromiumsrcbasetrace_eventcommon.git",
        revision: "147f65333c38ddd1ebf554e89965c243c8ce50b3"
  end

  resource "v8build" do
    url "https:chromium.googlesource.comchromiumsrcbuild.git",
        revision: "afe0125ef9e10b400d9ec145aa18fca932369346"
  end

  resource "v8third_partygoogletestsrc" do
    url "https:chromium.googlesource.comexternalgithub.comgooglegoogletest.git",
        revision: "af29db7ec28d6df1c7f0f745186884091e602e07"
  end

  resource "v8third_partyicu" do
    url "https:chromium.googlesource.comchromiumdepsicu.git",
        revision: "de4ce0071eb47ed54cbda54869001210cf3a8ae5"
  end

  resource "v8third_partyjinja2" do
    url "https:chromium.googlesource.comchromiumsrcthird_partyjinja2.git",
        revision: "515dd10de9bf63040045902a4a310d2ba25213a0"
  end

  resource "v8third_partymarkupsafe" do
    url "https:chromium.googlesource.comchromiumsrcthird_partymarkupsafe.git",
        revision: "006709ba3ed87660a17bd4548c45663628f5ed85"
  end

  resource "v8third_partyzlib" do
    url "https:chromium.googlesource.comchromiumsrcthird_partyzlib.git",
        revision: "526382e41c9c5275dc329db4328b54e4f344a204"
  end

  resource "v8third_partyabseil-cpp" do
    url "https:chromium.googlesource.comchromiumsrcthird_partyabseil-cpp.git",
        revision: "583dc6d1b3a0dd44579718699e37cad2f0c41a26"
  end

  def install
    (buildpath"build").install resource("v8build")
    (buildpath"third_partyjinja2").install resource("v8third_partyjinja2")
    (buildpath"third_partymarkupsafe").install resource("v8third_partymarkupsafe")
    (buildpath"third_partygoogletestsrc").install resource("v8third_partygoogletestsrc")
    (buildpath"basetrace_eventcommon").install resource("v8basetrace_eventcommon")
    (buildpath"third_partyicu").install resource("v8third_partyicu")
    (buildpath"third_partyzlib").install resource("v8third_partyzlib")
    (buildpath"third_partyabseil-cpp").install resource("v8third_partyabseil-cpp")

    # Build gn from source and add it to the PATH
    (buildpath"gn").install resource("gn")
    cd "gn" do
      system "python3.11", "buildgen.py"
      system "ninja", "-C", "out", "gn"
    end
    ENV.prepend_path "PATH", buildpath"gnout"

    # create gclient_args.gni
    (buildpath"buildconfiggclient_args.gni").write <<~EOS
      declare_args() {
        checkout_google_benchmark = false
      }
    EOS

    # setup gn args
    gn_args = {
      is_debug:                     false,
      is_component_build:           true,
      v8_use_external_startup_data: false,
      v8_enable_i18n_support:       true, # enables i18n support with icu
      clang_base_path:              "\"#{Formula["llvm"].opt_prefix}\"", # uses Homebrew clang instead of Google clang
      clang_use_chrome_plugins:     false, # disable the usage of Google's custom clang plugins
      use_custom_libcxx:            false, # uses system libc++ instead of Google's custom one
      treat_warnings_as_errors:     false, # ignore not yet supported clang argument warnings
      use_lld:                      false, # upstream use LLD but this leads to build failure on ARM
    }

    if OS.linux?
      gn_args[:is_clang] = false # use GCC on Linux
      gn_args[:use_sysroot] = false # don't use sysroot
      gn_args[:custom_toolchain] = "\"buildtoolchainlinuxunbundle:default\"" # uses system toolchain
      gn_args[:host_toolchain] = "\"buildtoolchainlinuxunbundle:default\"" # to respect passed LDFLAGS
      ENV["AR"] = DevelopmentTools.locate("ar")
      ENV["NM"] = DevelopmentTools.locate("nm")
      gn_args[:use_rbe] = false
    end

    # Make sure private libraries can be found from lib
    ENV.prepend "LDFLAGS", "-Wl,-rpath,#{rpath(target: libexec)}"

    # Transform to args string
    gn_args_string = gn_args.map { |k, v| "#{k}=#{v}" }.join(" ")

    # Build with gn + ninja
    system "gn", "gen", "--args=#{gn_args_string}", "out.gn"
    system "ninja", "-j", ENV.make_jobs, "-C", "out.gn", "-v", "d8"

    # Install libraries and headers into libexec so d8 can find them, and into standard directories
    # so other packages can find them and they are linked into HOMEBREW_PREFIX
    libexec.install "include"

    # Make sure we don't symlink non-headers into `include`.
    header_files_and_directories = (libexec"include").children.select do |child|
      (child.extname == ".h") || child.directory?
    end
    include.install_symlink header_files_and_directories

    libexec.install "out.gnd8", "out.gnicudtl.dat"
    bin.write_exec_script libexec"d8"

    libexec.install Pathname.glob("out.gn#{shared_library("*")}")
    lib.install_symlink libexec.glob(shared_library("libv8*"))
    lib.glob("*.TOC").map(&:unlink) if OS.linux? # Remove symlinks to .so.TOC text files
  end

  test do
    assert_equal "Hello World!", shell_output("#{bin}d8 -e 'print(\"Hello World!\");'").chomp
    t = "#{bin}d8 -e 'print(new Intl.DateTimeFormat(\"en-US\").format(new Date(\"2012-12-20T03:00:00\")));'"
    assert_match %r{12\d{2}2012}, shell_output(t).chomp

    (testpath"test.cpp").write <<~EOS
      #include <libplatformlibplatform.h>
      #include <v8.h>
      int main(){
        static std::unique_ptr<v8::Platform> platform = v8::platform::NewDefaultPlatform();
        v8::V8::InitializePlatform(platform.get());
        v8::V8::Initialize();
        return 0;
      }
    EOS

    # link against installed libc++
    system ENV.cxx, "-std=c++17", "test.cpp",
                    "-I#{include}", "-L#{lib}",
                    "-Wl,-rpath,#{libexec}",
                    "-lv8", "-lv8_libplatform"
  end
end