  include Language::Python::Virtualenv

  desc "Locking and sync for Pip requirements files"
  homepage "https://pip-tools.readthedocs.io"
  url "https://files.pythonhosted.org/packages/fd/01/f0055058a86a888f32ac794fa68d5a25c2d2f7a3e8181474b711faaa2145/pip-tools-7.3.0.tar.gz"
  sha256 "8e9c99127fe024c025b46a0b2d15c7bd47f18f33226cf7330d35493663fc1d1d"
  license "BSD-3-Clause"

  bottle do
    rebuild 2
    sha256 cellar: :any_skip_relocation, arm64_sonoma:   "07b13c198340250aba34486f574109926050bb1cb2a03d762bfa1ca67f6b34ba"
    sha256 cellar: :any_skip_relocation, arm64_ventura:  "f2969e4646148a61f0f21a037faad122d75714810c384db7df05b801d32c1b14"
    sha256 cellar: :any_skip_relocation, arm64_monterey: "e70ac4e131358045306667122f6c2a9bdaef3a8cb56d5a86cae5ce89df793e22"
    sha256 cellar: :any_skip_relocation, sonoma:         "35495278c98dd27e460f22c78472808c082145705213f84f6da8d38a93e32261"
    sha256 cellar: :any_skip_relocation, ventura:        "b2fd59d9c1a4e8376e88a2e39ddf1bb661e5bae65a8176e4b29a13cd3659ab31"
    sha256 cellar: :any_skip_relocation, monterey:       "0099470b82d3fda7585fbe789da6557369b115820bc56aaad32cc3880a92480c"
    sha256 cellar: :any_skip_relocation, x86_64_linux:   "641b51626ea1908bdff7db13d9519cf6289aa5bfc20b64cdefa5a1917a864e2e"
  end

  depends_on "python-build"
  depends_on "python-packaging"
  depends_on "python@3.11"

  resource "click" do
    url "https://files.pythonhosted.org/packages/72/bd/fedc277e7351917b6c4e0ac751853a97af261278a4c7808babafa8ef2120/click-8.1.6.tar.gz"
    sha256 "48ee849951919527a045bfe3bf7baa8a959c423134e1a5b98c05c20ba75a1cbd"
  end

  resource "pyproject-hooks" do
    url "https://files.pythonhosted.org/packages/25/c1/374304b8407d3818f7025457b7366c8e07768377ce12edfe2aa58aa0f64c/pyproject_hooks-1.0.0.tar.gz"
    sha256 "f271b298b97f5955d53fb12b72c1fb1948c22c1a6b70b315c54cedaca0264ef5"
  end

  resource "wheel" do
    url "https://files.pythonhosted.org/packages/c9/3d/02a14af2b413d7abf856083f327744d286f4468365cddace393a43d9d540/wheel-0.41.1.tar.gz"
    sha256 "12b911f083e876e10c595779709f8a88a59f45aacc646492a67fe9ef796c1b47"
  end

  def install
    virtualenv_install_with_resources

    %w[pip-compile pip-sync].each do |script|
      generate_completions_from_executable(bin/script, shells: [:fish, :zsh], shell_parameter_format: :click)
    end
  end

  test do
    (testpath/"requirements.in").write <<~EOS
      pip-tools
      typing-extensions
    EOS

    compiled = shell_output("#{bin}/pip-compile requirements.in -q -o -")
    assert_match "This file is autogenerated by pip-compile", compiled
    assert_match "# via pip-tools", compiled
  end
end