class Qcachegrind < Formula
  desc "Visualize data generated by Cachegrind and Calltree"
  homepage "https://apps.kde.org/kcachegrind/"
  url "https://download.kde.org/stable/release-service/25.08.1/src/kcachegrind-25.08.1.tar.xz"
  sha256 "65eb86ee20cfe5f9c68554e20758fa907cfc9851808b14415192612560f67085"
  license "GPL-2.0-or-later"
  head "https://invent.kde.org/sdk/kcachegrind.git", branch: "master"

  # We don't match versions like 19.07.80 or 19.07.90 where the patch number
  # is 80+ (beta) or 90+ (RC), as these aren't stable releases.
  livecheck do
    url "https://download.kde.org/stable/release-service/"
    regex(%r{href=.*?v?(\d+\.\d+\.(?:(?![89]\d)\d+)(?:\.\d+)*)/?["' >]}i)
  end

  bottle do
    sha256 cellar: :any,                 arm64_tahoe:   "424ac5ecc534cd7b54dc2af372b6cb04175c7bc5c674f3f0d96cb3964d7c9426"
    sha256 cellar: :any,                 arm64_sequoia: "b16db89e164f93666735793a546ec759886eae216064f58b4a48d9aaae30392d"
    sha256 cellar: :any,                 arm64_sonoma:  "5d9bcf738d3fcaae9174e20ecb908411cf97fbe389e0b7b9e56a09bed8dd21da"
    sha256 cellar: :any,                 arm64_ventura: "1ed635951e37a8a133c2e1f148491863c57bbc682b6a508eaf468a859357f91c"
    sha256 cellar: :any,                 sonoma:        "fda1553797fe5d48c33ebc78214d7ebf91a01ad4ab0061c9a6b0cc530de5a3f4"
    sha256 cellar: :any,                 ventura:       "ee61d9be61566f91b5ff85e5d0e02fc741624ca05889571ee108caf6fa086674"
    sha256 cellar: :any_skip_relocation, x86_64_linux:  "fef398844ede335df0781d4f7660bdded9b64ae1f3c7e58873266caac78f55bd"
  end

  depends_on "graphviz"
  depends_on "qt"

  def install
    args = %w[-config release]
    if OS.mac?
      spec = (ENV.compiler == :clang) ? "macx-clang" : "macx-g++"
      args += %W[-spec #{spec}]
    end

    qt = Formula["qt"]
    system qt.opt_bin/"qmake", *args
    system "make"

    if OS.mac?
      prefix.install "qcachegrind/qcachegrind.app"
      bin.install_symlink prefix/"qcachegrind.app/Contents/MacOS/qcachegrind"
    else
      bin.install "qcachegrind/qcachegrind"
    end
  end
end