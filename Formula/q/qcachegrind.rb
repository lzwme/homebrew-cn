class Qcachegrind < Formula
  desc "Visualize data generated by Cachegrind and Calltree"
  homepage "https://apps.kde.org/kcachegrind/"
  url "https://download.kde.org/stable/release-service/26.04.0/src/kcachegrind-26.04.0.tar.xz"
  sha256 "476919c3a9bec436b1a55a6c1a28ebfb07b43d6e22c05c6a476d0bfa537c8fa4"
  license "GPL-2.0-or-later"
  head "https://invent.kde.org/sdk/kcachegrind.git", branch: "master"

  # We don't match versions like 19.07.80 or 19.07.90 where the patch number
  # is 80+ (beta) or 90+ (RC), as these aren't stable releases.
  livecheck do
    url "https://download.kde.org/stable/release-service/"
    regex(%r{href=.*?v?(\d+\.\d+\.(?:(?![89]\d)\d+)(?:\.\d+)*)/?["' >]}i)
  end

  bottle do
    sha256 cellar: :any,                 arm64_tahoe:   "c1638af3ff165f105d12914ecf3476f863113dd15551f88722d33505d5c339ab"
    sha256 cellar: :any,                 arm64_sequoia: "3cd03d10384e3f5a8cd1961e66eca8213a00d0070e5eda7cec09e967f2f8ec02"
    sha256 cellar: :any,                 arm64_sonoma:  "06146e2f667bb471559fad88259eefce6e1fe84581c4568706aaeac62a4baaef"
    sha256 cellar: :any,                 sonoma:        "b255d9e1ab05065b1a1a5828ee5c664c8c44694fd9bd1f50ab50d12631a54ec9"
    sha256 cellar: :any_skip_relocation, arm64_linux:   "f54155c5997291a66e718d57a753503dc21d7d701ff9778b7476eff7fcdc44e2"
    sha256 cellar: :any_skip_relocation, x86_64_linux:  "acf8d96d9b0c91d1eb99410682919361c240e4313a2b09357ce3d4bb218eef71"
  end

  depends_on "graphviz"
  depends_on "qtbase"

  def install
    args = %w[-config release]
    if OS.mac?
      spec = (ENV.compiler == :clang) ? "macx-clang" : "macx-g++"
      args += %W[-spec #{spec}]
    end

    qt = Formula["qtbase"]
    system qt.opt_bin/"qmake", *args
    system "make"

    if OS.mac?
      prefix.install "qcachegrind/qcachegrind.app"
      bin.install_symlink prefix/"qcachegrind.app/Contents/MacOS/qcachegrind"
    else
      bin.install "qcachegrind/qcachegrind"
    end
  end
end