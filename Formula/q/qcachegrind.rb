class Qcachegrind < Formula
  desc "Visualize data generated by Cachegrind and Calltree"
  homepage "https://apps.kde.org/kcachegrind/"
  url "https://download.kde.org/stable/release-service/26.04.1/src/kcachegrind-26.04.1.tar.xz"
  sha256 "7b35afb0a796e7d54c1fb06940b58de60aa8eeb7badec6a14eb848add7072dcf"
  license "GPL-2.0-or-later"
  head "https://invent.kde.org/sdk/kcachegrind.git", branch: "master"

  # We don't match versions like 19.07.80 or 19.07.90 where the patch number
  # is 80+ (beta) or 90+ (RC), as these aren't stable releases.
  livecheck do
    url "https://download.kde.org/stable/release-service/"
    regex(%r{href=.*?v?(\d+\.\d+\.(?:(?![89]\d)\d+)(?:\.\d+)*)/?["' >]}i)
  end

  bottle do
    sha256 cellar: :any,                 arm64_tahoe:   "007e70162056981802b17fac36d9aad6cea16f43fa999d01bbcabfe75a5797d2"
    sha256 cellar: :any,                 arm64_sequoia: "5d591cefbbe42fdb4bb871aee67c6032f4862a0d1e5430424390e509c0cbf92f"
    sha256 cellar: :any,                 arm64_sonoma:  "03de083c47aa416e71ee8160ca06abab96fcca1b15cfaddfba47115e32b3d98e"
    sha256 cellar: :any,                 sonoma:        "28f3df167290de82bb95ebe6ec24888922d945b0e95aed689fd60e2534bc2114"
    sha256 cellar: :any_skip_relocation, arm64_linux:   "2ee6ca74b538b7b0236dd4b16f77e7a2ec67956057e04e901b51830d38c2911f"
    sha256 cellar: :any_skip_relocation, x86_64_linux:  "a62885e1482763a911db6d7ccf504c29f41852ae366207153f06d289855955b7"
  end

  depends_on "graphviz"
  depends_on "qtbase"

  def install
    args = %w[-config release]
    if OS.mac?
      spec = (ENV.compiler == :clang) ? "macx-clang" : "macx-g++"
      args += %W[-spec #{spec}]
    end

    qt = Formula["qtbase"]
    system qt.opt_bin/"qmake", *args
    system "make"

    if OS.mac?
      prefix.install "qcachegrind/qcachegrind.app"
      bin.install_symlink prefix/"qcachegrind.app/Contents/MacOS/qcachegrind"
    else
      bin.install "qcachegrind/qcachegrind"
    end
  end
end