class Qcachegrind < Formula
  desc "Visualize data generated by Cachegrind and Calltree"
  homepage "https://apps.kde.org/kcachegrind/"
  url "https://download.kde.org/stable/release-service/25.12.3/src/kcachegrind-25.12.3.tar.xz"
  sha256 "e96acc0d688b12940ecc8896ab89ffd158bdf595fc69ad6d4a380e28e2e8efd2"
  license "GPL-2.0-or-later"
  head "https://invent.kde.org/sdk/kcachegrind.git", branch: "master"

  # We don't match versions like 19.07.80 or 19.07.90 where the patch number
  # is 80+ (beta) or 90+ (RC), as these aren't stable releases.
  livecheck do
    url "https://download.kde.org/stable/release-service/"
    regex(%r{href=.*?v?(\d+\.\d+\.(?:(?![89]\d)\d+)(?:\.\d+)*)/?["' >]}i)
  end

  bottle do
    sha256 cellar: :any,                 arm64_tahoe:   "114b581a23c5a1083ea5421dbbd1d59aa388ce7c457bcd2290a906d85ac1d9c6"
    sha256 cellar: :any,                 arm64_sequoia: "efd015111041edba2f202a0d7d207a4a66fae8a0be0dba13ade8c8248f839ee7"
    sha256 cellar: :any,                 arm64_sonoma:  "50673c3743bf71df0f57a99776edc98acd1d6adbb4a71318cb29a20e213fd28e"
    sha256 cellar: :any,                 sonoma:        "15cfc8f58a7320f1e0f2cdbc1ae6a3afc57f4f08069874dd6845c363eb16df86"
    sha256 cellar: :any_skip_relocation, arm64_linux:   "fb09053c59d59e3421b79aa103b43296ae9296da7e05b9e46fff371196cf4825"
    sha256 cellar: :any_skip_relocation, x86_64_linux:  "5b30a0b2f88f9cd3adb5d2b042078110e94255c3e1470abddb927b96f599f864"
  end

  depends_on "graphviz"
  depends_on "qtbase"

  def install
    args = %w[-config release]
    if OS.mac?
      spec = (ENV.compiler == :clang) ? "macx-clang" : "macx-g++"
      args += %W[-spec #{spec}]
    end

    qt = Formula["qtbase"]
    system qt.opt_bin/"qmake", *args
    system "make"

    if OS.mac?
      prefix.install "qcachegrind/qcachegrind.app"
      bin.install_symlink prefix/"qcachegrind.app/Contents/MacOS/qcachegrind"
    else
      bin.install "qcachegrind/qcachegrind"
    end
  end
end