class Qcachegrind < Formula
  desc "Visualize data generated by Cachegrind and Calltree"
  homepage "https://apps.kde.org/kcachegrind/"
  url "https://download.kde.org/stable/release-service/24.02.1/src/kcachegrind-24.02.1.tar.xz"
  sha256 "74dd958c439249bbfb3e6a8f08210733887a6f98157ed32fe6c26fd402f71015"
  license "GPL-2.0-or-later"
  head "https://invent.kde.org/sdk/kcachegrind.git", branch: "master"

  # We don't match versions like 19.07.80 or 19.07.90 where the patch number
  # is 80+ (beta) or 90+ (RC), as these aren't stable releases.
  livecheck do
    url "https://download.kde.org/stable/release-service/"
    regex(%r{href=.*?v?(\d+\.\d+\.(?:(?![89]\d)\d+)(?:\.\d+)*)/?["' >]}i)
  end

  bottle do
    sha256 cellar: :any,                 arm64_sonoma:   "2b4e06d5c2d0445ad16a5608b5537651db22fd39ea3d20fe93d966c51a94ed75"
    sha256 cellar: :any,                 arm64_ventura:  "3efd2ad03fe8cd9f4f26163e09fbcb633c9e6bfaafdd440509adc33670ce3cc1"
    sha256 cellar: :any,                 arm64_monterey: "cf8fa95bc564dc687704817364f47f6537d32ac745a35440dcb9cdd64fc02c7f"
    sha256 cellar: :any,                 sonoma:         "7bb9c011e636b0f3acefc913943b79a35c0f871882df87c7d9070699ff28346b"
    sha256 cellar: :any,                 ventura:        "2574a0142c727c697fae78c564e3ba5dffc0772e95821a7324dffd74aadae49b"
    sha256 cellar: :any,                 monterey:       "e2ede736f4c45715c386fff7482ee1ba034ec3158c146ec555f66601a6d7d286"
    sha256 cellar: :any_skip_relocation, x86_64_linux:   "bcbcdbb51ba64095b09eade37bfccba589df75007eb110d7f255847ceab4cf98"
  end

  depends_on "graphviz"
  depends_on "qt"

  fails_with gcc: "5"

  def install
    args = %w[-config release]
    if OS.mac?
      spec = (ENV.compiler == :clang) ? "macx-clang" : "macx-g++"
      args += %W[-spec #{spec}]
    end

    qt = Formula["qt"]
    system qt.opt_bin/"qmake", *args
    system "make"

    if OS.mac?
      prefix.install "qcachegrind/qcachegrind.app"
      bin.install_symlink prefix/"qcachegrind.app/Contents/MacOS/qcachegrind"
    else
      bin.install "qcachegrind/qcachegrind"
    end
  end
end