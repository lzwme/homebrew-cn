class Qcachegrind < Formula
  desc "Visualize data generated by Cachegrind and Calltree"
  homepage "https://apps.kde.org/kcachegrind/"
  url "https://download.kde.org/stable/release-service/25.04.1/src/kcachegrind-25.04.1.tar.xz"
  sha256 "0b7349b84f5e90a9d9cb05b2642b2a79c27a629dacb85ba94e57b9528be1f975"
  license "GPL-2.0-or-later"
  head "https://invent.kde.org/sdk/kcachegrind.git", branch: "master"

  # We don't match versions like 19.07.80 or 19.07.90 where the patch number
  # is 80+ (beta) or 90+ (RC), as these aren't stable releases.
  livecheck do
    url "https://download.kde.org/stable/release-service/"
    regex(%r{href=.*?v?(\d+\.\d+\.(?:(?![89]\d)\d+)(?:\.\d+)*)/?["' >]}i)
  end

  bottle do
    sha256 cellar: :any,                 arm64_sonoma:  "51cb12d4d28aba1779e55a2abf0ded2e924f24f67427ebe8aba30831e7c1869b"
    sha256 cellar: :any,                 arm64_ventura: "0b0d9cd39dd7f200f5d33c992ffba32a58ba28d3ff4d11944ee961739f9d2261"
    sha256 cellar: :any,                 sonoma:        "e0fc44aac494bba334a51586681b8158f21b73df7bd8d35d5d99caee99c23fb2"
    sha256 cellar: :any,                 ventura:       "914e283c91088f2dbf0df3378277116f401a083563f7dadda7c85f5307063365"
    sha256 cellar: :any_skip_relocation, x86_64_linux:  "4f478ae21aa1fa2b484124a399d5a47f82a0474484e9f6ca3843eaf9ed06801d"
  end

  depends_on "graphviz"
  depends_on "qt"

  def install
    args = %w[-config release]
    if OS.mac?
      spec = (ENV.compiler == :clang) ? "macx-clang" : "macx-g++"
      args += %W[-spec #{spec}]
    end

    qt = Formula["qt"]
    system qt.opt_bin/"qmake", *args
    system "make"

    if OS.mac?
      prefix.install "qcachegrind/qcachegrind.app"
      bin.install_symlink prefix/"qcachegrind.app/Contents/MacOS/qcachegrind"
    else
      bin.install "qcachegrind/qcachegrind"
    end
  end
end