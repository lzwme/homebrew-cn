class Qcachegrind < Formula
  desc "Visualize data generated by Cachegrind and Calltree"
  homepage "https://apps.kde.org/kcachegrind/"
  url "https://download.kde.org/stable/release-service/25.08.2/src/kcachegrind-25.08.2.tar.xz"
  sha256 "8d50c071a23db62acecdf22d6e94396aa03e4d5ff97acc7d2be84863b34fbfc8"
  license "GPL-2.0-or-later"
  head "https://invent.kde.org/sdk/kcachegrind.git", branch: "master"

  # We don't match versions like 19.07.80 or 19.07.90 where the patch number
  # is 80+ (beta) or 90+ (RC), as these aren't stable releases.
  livecheck do
    url "https://download.kde.org/stable/release-service/"
    regex(%r{href=.*?v?(\d+\.\d+\.(?:(?![89]\d)\d+)(?:\.\d+)*)/?["' >]}i)
  end

  bottle do
    sha256 cellar: :any,                 arm64_tahoe:   "2b75a14bf0a31e5b78aac0b6ea3a844ed6616183e75a05c6132e9d7856190c92"
    sha256 cellar: :any,                 arm64_sequoia: "39d1839e8c09d71be121a016242caca0d5bf4e2beae51deff8df319a32183f3f"
    sha256 cellar: :any,                 arm64_sonoma:  "f93c5d2f1aa6298d9946b49b7e0f711631605ce26b94757957ea477e54cfc456"
    sha256 cellar: :any,                 sonoma:        "8c474db773a55ffc8231a626380603b634db50556a0f1a91da1a58ddf4704899"
    sha256 cellar: :any_skip_relocation, x86_64_linux:  "d86cf913e1e58d2f6a3f74cf0ea02e3bf423a3f3aa5231b34f27e9c4b2bb8f1f"
  end

  depends_on "graphviz"
  depends_on "qtbase"

  def install
    args = %w[-config release]
    if OS.mac?
      spec = (ENV.compiler == :clang) ? "macx-clang" : "macx-g++"
      args += %W[-spec #{spec}]
    end

    qt = Formula["qtbase"]
    system qt.opt_bin/"qmake", *args
    system "make"

    if OS.mac?
      prefix.install "qcachegrind/qcachegrind.app"
      bin.install_symlink prefix/"qcachegrind.app/Contents/MacOS/qcachegrind"
    else
      bin.install "qcachegrind/qcachegrind"
    end
  end
end