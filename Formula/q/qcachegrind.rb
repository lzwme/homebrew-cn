class Qcachegrind < Formula
  desc "Visualize data generated by Cachegrind and Calltree"
  homepage "https://apps.kde.org/kcachegrind/"
  url "https://download.kde.org/stable/release-service/25.08.1/src/kcachegrind-25.08.1.tar.xz"
  sha256 "65eb86ee20cfe5f9c68554e20758fa907cfc9851808b14415192612560f67085"
  license "GPL-2.0-or-later"
  head "https://invent.kde.org/sdk/kcachegrind.git", branch: "master"

  # We don't match versions like 19.07.80 or 19.07.90 where the patch number
  # is 80+ (beta) or 90+ (RC), as these aren't stable releases.
  livecheck do
    url "https://download.kde.org/stable/release-service/"
    regex(%r{href=.*?v?(\d+\.\d+\.(?:(?![89]\d)\d+)(?:\.\d+)*)/?["' >]}i)
  end

  bottle do
    rebuild 1
    sha256 cellar: :any,                 arm64_tahoe:   "ef7df3f20baa37dca0b57b60571a5f6e4378f837025e115cebaa7b4b2c6ec10c"
    sha256 cellar: :any,                 arm64_sequoia: "b9f198d22ecaec1c636c7ae1650c68e98f3891c36a62d6fe48c2ce842d3b882a"
    sha256 cellar: :any,                 arm64_sonoma:  "1eee9605500bfb7d11198e2b07b1de137137eda6a55bea2d79e381939dfb4f31"
    sha256 cellar: :any,                 sonoma:        "870107303e9f5db717cd3c9834120f9df68a94e1dccc21f8058e53aef07f4b20"
    sha256 cellar: :any_skip_relocation, x86_64_linux:  "e58e897228a09ca2825630b2f3b3a40d432c3da6293f3f18a3ff819384f9dcd7"
  end

  depends_on "graphviz"
  depends_on "qtbase"

  def install
    args = %w[-config release]
    if OS.mac?
      spec = (ENV.compiler == :clang) ? "macx-clang" : "macx-g++"
      args += %W[-spec #{spec}]
    end

    qt = Formula["qtbase"]
    system qt.opt_bin/"qmake", *args
    system "make"

    if OS.mac?
      prefix.install "qcachegrind/qcachegrind.app"
      bin.install_symlink prefix/"qcachegrind.app/Contents/MacOS/qcachegrind"
    else
      bin.install "qcachegrind/qcachegrind"
    end
  end
end