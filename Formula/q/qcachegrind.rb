class Qcachegrind < Formula
  desc "Visualize data generated by Cachegrind and Calltree"
  homepage "https://apps.kde.org/kcachegrind/"
  url "https://download.kde.org/stable/release-service/25.12.0/src/kcachegrind-25.12.0.tar.xz"
  sha256 "76696fdedd0add62691191bcc51ede39cd42ee31a1724de43f38a4cbb5bdfc08"
  license "GPL-2.0-or-later"
  head "https://invent.kde.org/sdk/kcachegrind.git", branch: "master"

  # We don't match versions like 19.07.80 or 19.07.90 where the patch number
  # is 80+ (beta) or 90+ (RC), as these aren't stable releases.
  livecheck do
    url "https://download.kde.org/stable/release-service/"
    regex(%r{href=.*?v?(\d+\.\d+\.(?:(?![89]\d)\d+)(?:\.\d+)*)/?["' >]}i)
  end

  bottle do
    sha256 cellar: :any,                 arm64_tahoe:   "a3c7d1e33a48178132950eeb1a4c9c353c63df08f9e3fdf95fe8f9d130257c26"
    sha256 cellar: :any,                 arm64_sequoia: "296e5f80dc656ec07c94c17753049b69c21d400dcb2dcc499fd5fe3c2469edc4"
    sha256 cellar: :any,                 arm64_sonoma:  "6c8a6c2eed4f59704bc4b15ad8894760b67dce662affc13f500292935063cdbf"
    sha256 cellar: :any,                 sonoma:        "b3924f5084f0f03f5327e5fdbe69a9b06583438ff4edc53be18f6be651162c01"
    sha256 cellar: :any_skip_relocation, arm64_linux:   "ba35901cceb6e25c31f8a8c47dc3aeee3b09e309bf574015cb66d30cb7c11051"
    sha256 cellar: :any_skip_relocation, x86_64_linux:  "f461b73cce020ef13d27d9097d3945285762b1e45f0bf666fa35ec4281eff0bd"
  end

  depends_on "graphviz"
  depends_on "qtbase"

  def install
    args = %w[-config release]
    if OS.mac?
      spec = (ENV.compiler == :clang) ? "macx-clang" : "macx-g++"
      args += %W[-spec #{spec}]
    end

    qt = Formula["qtbase"]
    system qt.opt_bin/"qmake", *args
    system "make"

    if OS.mac?
      prefix.install "qcachegrind/qcachegrind.app"
      bin.install_symlink prefix/"qcachegrind.app/Contents/MacOS/qcachegrind"
    else
      bin.install "qcachegrind/qcachegrind"
    end
  end
end