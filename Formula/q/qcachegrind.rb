class Qcachegrind < Formula
  desc "Visualize data generated by Cachegrind and Calltree"
  homepage "https://apps.kde.org/kcachegrind/"
  url "https://download.kde.org/stable/release-service/26.04.2/src/kcachegrind-26.04.2.tar.xz"
  sha256 "89a4ee7ec8b0750067cc6e1826eab185cb97dd6b1b9ebcc04e5528b05e3b7078"
  license "GPL-2.0-or-later"
  head "https://invent.kde.org/sdk/kcachegrind.git", branch: "master"

  # We don't match versions like 19.07.80 or 19.07.90 where the patch number
  # is 80+ (beta) or 90+ (RC), as these aren't stable releases.
  livecheck do
    url "https://download.kde.org/stable/release-service/"
    regex(%r{href=.*?v?(\d+\.\d+\.(?:(?![89]\d)\d+)(?:\.\d+)*)/?["' >]}i)
  end

  bottle do
    sha256 cellar: :any, arm64_tahoe:   "4daa2dc661c28c6ac72499808f3d79739e9012c5f93667ed4c2d3ae7e817e46b"
    sha256 cellar: :any, arm64_sequoia: "f87c4dcc752d7de53111588908e44a3366afbba40d9439ebe7ba6f4b6aeab6b2"
    sha256 cellar: :any, arm64_sonoma:  "0bbdd9a9a99c81b77bc9ceee67c9520d023b6bb1314a642aaa6ffe39d7c150f2"
    sha256 cellar: :any, sonoma:        "da349b85a081f1550d096813d6e1895fc74b8fc03f8b1aa6a73db6fb635132ce"
    sha256 cellar: :any, arm64_linux:   "c3185a86576eaf474df5de190da092eddb8b5648c750535f6034910d8ac03405"
    sha256 cellar: :any, x86_64_linux:  "12f97dc7034f46db243b492fcbb2cd62940748f84af078f16cedb1bcb262cafb"
  end

  depends_on "graphviz"
  depends_on "qtbase"

  def install
    args = %w[-config release]
    if OS.mac?
      spec = (ENV.compiler == :clang) ? "macx-clang" : "macx-g++"
      args += %W[-spec #{spec}]
    end

    qt = Formula["qtbase"]
    system qt.opt_bin/"qmake", *args
    system "make"

    if OS.mac?
      prefix.install "qcachegrind/qcachegrind.app"
      bin.install_symlink prefix/"qcachegrind.app/Contents/MacOS/qcachegrind"
    else
      bin.install "qcachegrind/qcachegrind"
    end
  end
end