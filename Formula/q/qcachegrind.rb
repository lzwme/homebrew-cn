class Qcachegrind < Formula
  desc "Visualize data generated by Cachegrind and Calltree"
  homepage "https://apps.kde.org/kcachegrind/"
  url "https://download.kde.org/stable/release-service/25.12.1/src/kcachegrind-25.12.1.tar.xz"
  sha256 "f291cddad712e84deb19202aa8641d6950cfd52228df5f3341b76bfc5768737d"
  license "GPL-2.0-or-later"
  head "https://invent.kde.org/sdk/kcachegrind.git", branch: "master"

  # We don't match versions like 19.07.80 or 19.07.90 where the patch number
  # is 80+ (beta) or 90+ (RC), as these aren't stable releases.
  livecheck do
    url "https://download.kde.org/stable/release-service/"
    regex(%r{href=.*?v?(\d+\.\d+\.(?:(?![89]\d)\d+)(?:\.\d+)*)/?["' >]}i)
  end

  bottle do
    sha256 cellar: :any,                 arm64_tahoe:   "f07e98e4271438854d61114c079dc44379b619ac8a3bc4de1c5b11478d4a2612"
    sha256 cellar: :any,                 arm64_sequoia: "da3d14a55ca58deff82c7ac48b6188c9b345b7179ad091262aa4308c88bb4dc6"
    sha256 cellar: :any,                 arm64_sonoma:  "32662c6bc6381bff849a50d431a7ed7a979f0fd055d991439950b9ad1ac6ee20"
    sha256 cellar: :any,                 sonoma:        "4e92320333ebc955e7ead98b40519191dbaadbcc759b5a1c3dff063bd37c418e"
    sha256 cellar: :any_skip_relocation, arm64_linux:   "46490dc36555e5e691ec742b0a581830329370f93f2379d54d91961650a5836c"
    sha256 cellar: :any_skip_relocation, x86_64_linux:  "2f521ef89c4de64d90cefb858d5844aceee8e27da96badd11f3b121184720ce4"
  end

  depends_on "graphviz"
  depends_on "qtbase"

  def install
    args = %w[-config release]
    if OS.mac?
      spec = (ENV.compiler == :clang) ? "macx-clang" : "macx-g++"
      args += %W[-spec #{spec}]
    end

    qt = Formula["qtbase"]
    system qt.opt_bin/"qmake", *args
    system "make"

    if OS.mac?
      prefix.install "qcachegrind/qcachegrind.app"
      bin.install_symlink prefix/"qcachegrind.app/Contents/MacOS/qcachegrind"
    else
      bin.install "qcachegrind/qcachegrind"
    end
  end
end