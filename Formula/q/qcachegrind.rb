class Qcachegrind < Formula
  desc "Visualize data generated by Cachegrind and Calltree"
  homepage "https://apps.kde.org/kcachegrind/"
  url "https://download.kde.org/stable/release-service/25.08.3/src/kcachegrind-25.08.3.tar.xz"
  sha256 "aec838dfa806b438d198f5bb66a2a9925ecdb86d09da86c593a21302052b0bbe"
  license "GPL-2.0-or-later"
  head "https://invent.kde.org/sdk/kcachegrind.git", branch: "master"

  # We don't match versions like 19.07.80 or 19.07.90 where the patch number
  # is 80+ (beta) or 90+ (RC), as these aren't stable releases.
  livecheck do
    url "https://download.kde.org/stable/release-service/"
    regex(%r{href=.*?v?(\d+\.\d+\.(?:(?![89]\d)\d+)(?:\.\d+)*)/?["' >]}i)
  end

  bottle do
    sha256 cellar: :any,                 arm64_tahoe:   "0fbabeef5b2603ca4111f1e5871bb1ff8492c33e450911398c3cc4cab05cc78a"
    sha256 cellar: :any,                 arm64_sequoia: "5e915ae4cd7dc03d4670d88a08804b13fcd21b59da2e8959c2c13b872c74800a"
    sha256 cellar: :any,                 arm64_sonoma:  "a475a44621aa0258fd0f85a94d5a5fae67d25628bbf6edf9406be24fd27f85dc"
    sha256 cellar: :any,                 sonoma:        "ee0d39df4e4e31ca6d74f610ec4fb641c612778ebaf9dbbd80285711247be0c9"
    sha256 cellar: :any_skip_relocation, arm64_linux:   "1119fd949f1a17b1a70cbbb1ebb53bab4b935497baccfa6640bfecd68f7a4473"
    sha256 cellar: :any_skip_relocation, x86_64_linux:  "76eeace62588bf437be016b9817025f05dad9724e33c0129cfc8da9064ad30a8"
  end

  depends_on "graphviz"
  depends_on "qtbase"

  def install
    args = %w[-config release]
    if OS.mac?
      spec = (ENV.compiler == :clang) ? "macx-clang" : "macx-g++"
      args += %W[-spec #{spec}]
    end

    qt = Formula["qtbase"]
    system qt.opt_bin/"qmake", *args
    system "make"

    if OS.mac?
      prefix.install "qcachegrind/qcachegrind.app"
      bin.install_symlink prefix/"qcachegrind.app/Contents/MacOS/qcachegrind"
    else
      bin.install "qcachegrind/qcachegrind"
    end
  end
end