class Qcachegrind < Formula
  desc "Visualize data generated by Cachegrind and Calltree"
  homepage "https://apps.kde.org/kcachegrind/"
  url "https://download.kde.org/stable/release-service/24.05.0/src/kcachegrind-24.05.0.tar.xz"
  sha256 "22b1958cfc3ea635e08ac52247e35f7d20cfaea41c7ae8d65297f3e4f05a7cb3"
  license "GPL-2.0-or-later"
  head "https://invent.kde.org/sdk/kcachegrind.git", branch: "master"

  # We don't match versions like 19.07.80 or 19.07.90 where the patch number
  # is 80+ (beta) or 90+ (RC), as these aren't stable releases.
  livecheck do
    url "https://download.kde.org/stable/release-service/"
    regex(%r{href=.*?v?(\d+\.\d+\.(?:(?![89]\d)\d+)(?:\.\d+)*)/?["' >]}i)
  end

  bottle do
    sha256 cellar: :any,                 arm64_sonoma:   "966c83b3f26a00f189ec67b4e2f51d5db01a510d7e4b5f9cb48215c9a82fbc27"
    sha256 cellar: :any,                 arm64_ventura:  "ad0b9c43fd09c57eb3b06e4f4bb8a47443471324373ee84a65f62c279777f5b1"
    sha256 cellar: :any,                 arm64_monterey: "7357021f94b188b3de04b41808c56a2b74324752e5578e42c343ed23157ad5eb"
    sha256 cellar: :any,                 sonoma:         "f4751fcf7c8bc8f4be19a161b4574a33ade312ade19be217e24e15592494af84"
    sha256 cellar: :any,                 ventura:        "dee4891c85d2837947441a0622f31a043565df0127b5797a495a440ec671fe4a"
    sha256 cellar: :any,                 monterey:       "7bd606abe453d440afe083fbd0d811c5703c2d83d9082663a39d84642efb88a7"
    sha256 cellar: :any_skip_relocation, x86_64_linux:   "167df0928a09fed431c2b86a950ca0237f49dfda7c75889132e33bcbe8883323"
  end

  depends_on "graphviz"
  depends_on "qt"

  fails_with gcc: "5"

  def install
    args = %w[-config release]
    if OS.mac?
      spec = (ENV.compiler == :clang) ? "macx-clang" : "macx-g++"
      args += %W[-spec #{spec}]
    end

    qt = Formula["qt"]
    system qt.opt_bin/"qmake", *args
    system "make"

    if OS.mac?
      prefix.install "qcachegrind/qcachegrind.app"
      bin.install_symlink prefix/"qcachegrind.app/Contents/MacOS/qcachegrind"
    else
      bin.install "qcachegrind/qcachegrind"
    end
  end
end