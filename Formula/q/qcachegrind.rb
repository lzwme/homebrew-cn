class Qcachegrind < Formula
  desc "Visualize data generated by Cachegrind and Calltree"
  homepage "https://apps.kde.org/kcachegrind/"
  url "https://download.kde.org/stable/release-service/25.08.0/src/kcachegrind-25.08.0.tar.xz"
  sha256 "4253d8643a5e1c9261be470ac61c8f5eb2ce721036115ae204ac3aab6b38a0a4"
  license "GPL-2.0-or-later"
  head "https://invent.kde.org/sdk/kcachegrind.git", branch: "master"

  # We don't match versions like 19.07.80 or 19.07.90 where the patch number
  # is 80+ (beta) or 90+ (RC), as these aren't stable releases.
  livecheck do
    url "https://download.kde.org/stable/release-service/"
    regex(%r{href=.*?v?(\d+\.\d+\.(?:(?![89]\d)\d+)(?:\.\d+)*)/?["' >]}i)
  end

  bottle do
    sha256 cellar: :any,                 arm64_sonoma:  "55e63815b553a2024a8b8aec256aead46100b6af69dce686d636aa7ba4efff38"
    sha256 cellar: :any,                 arm64_ventura: "4b5289bd4156e5f472c34f88b566fba1191ab881694e3e0053218ffab2ebf9c4"
    sha256 cellar: :any,                 sonoma:        "0942e6b0f8b340991cffab2a910eb3e22221a8d1bee044b6717a17a498b6ca9b"
    sha256 cellar: :any,                 ventura:       "230a59f8e982ad6b49677a431e327b7bf03643796ee9cfae653a756e8235f662"
    sha256 cellar: :any_skip_relocation, x86_64_linux:  "f5aa1265294219c568251328d86dfa49fbeddb7e0c59f576a4b25dc64f411b08"
  end

  depends_on "graphviz"
  depends_on "qt"

  def install
    args = %w[-config release]
    if OS.mac?
      spec = (ENV.compiler == :clang) ? "macx-clang" : "macx-g++"
      args += %W[-spec #{spec}]
    end

    qt = Formula["qt"]
    system qt.opt_bin/"qmake", *args
    system "make"

    if OS.mac?
      prefix.install "qcachegrind/qcachegrind.app"
      bin.install_symlink prefix/"qcachegrind.app/Contents/MacOS/qcachegrind"
    else
      bin.install "qcachegrind/qcachegrind"
    end
  end
end