class JpegXl < Formula
  desc "New file format for still image compression"
  homepage "https:jpeg.orgjpegxlindex.html"
  url "https:github.comlibjxllibjxlarchiverefstagsv0.11.1.tar.gz"
  sha256 "1492dfef8dd6c3036446ac3b340005d92ab92f7d48ee3271b5dac1d36945d3d9"
  license "BSD-3-Clause"
  revision 1

  livecheck do
    url :stable
    regex(^v?(\d+(?:\.\d+)+)$i)
  end

  bottle do
    sha256 cellar: :any,                 arm64_sequoia: "c3aceba825400a7d64dbd95d622ce59f0414e54cdedb792afc51d9278dae65b6"
    sha256 cellar: :any,                 arm64_sonoma:  "d512c7b32e5c14337646f39e27187d4a241b8d7efaad2003b5cca543012c43fd"
    sha256 cellar: :any,                 arm64_ventura: "d4fc2bbf1f0568e60fb5c05b49dbbd9d989ede967b33d5d120894530f9861af3"
    sha256 cellar: :any,                 sonoma:        "5f403648ee1450951c0b2eaba9ea50be2599b35bdaf86f3fb7bf5b46d007e229"
    sha256 cellar: :any,                 ventura:       "d2e83758ff6899cc13588431ccfc1d00c63d317b6aad75316d76930ae139dd71"
    sha256 cellar: :any_skip_relocation, arm64_linux:   "dfc962a42c39eb4f6bac85124841d838fc97bd70710b3801fd9cae9d84422f09"
    sha256 cellar: :any_skip_relocation, x86_64_linux:  "e68aaa4bcb4ebad81d8ff0e3ef8556852e2a5434069ebcfb1887f34babc9075a"
  end

  depends_on "asciidoc" => :build
  depends_on "cmake" => :build
  depends_on "docbook-xsl" => :build
  depends_on "doxygen" => :build
  depends_on "pkgconf" => [:build, :test]
  depends_on "sphinx-doc" => :build
  depends_on "brotli"
  depends_on "giflib"
  depends_on "highway"
  depends_on "imath"
  depends_on "jpeg-turbo"
  depends_on "libpng"
  depends_on "little-cms2"
  depends_on "openexr"
  depends_on "webp"

  uses_from_macos "libxml2" => :build
  uses_from_macos "libxslt" => :build # for xsltproc
  uses_from_macos "python"

  # These resources are versioned according to the script supplied with jpeg-xl to download the dependencies:
  # https:github.comlibjxllibjxltreev#{version}third_party
  resource "sjpeg" do
    url "https:github.comwebmprojectsjpeg.git",
        revision: "94e0df6d0f8b44228de5be0ff35efb9f946a13c9"
  end

  def install
    ENV.append_path "XML_CATALOG_FILES", HOMEBREW_PREFIX"etcxmlcatalog"
    resources.each { |r| r.stage buildpath"third_party"r.name }
    system "cmake", "-S", ".", "-B", "build",
                    "-DJPEGXL_FORCE_SYSTEM_BROTLI=ON",
                    "-DJPEGXL_FORCE_SYSTEM_LCMS2=ON",
                    "-DJPEGXL_FORCE_SYSTEM_HWY=ON",
                    "-DJPEGXL_ENABLE_DEVTOOLS=ON",
                    "-DJPEGXL_ENABLE_JNI=OFF",
                    "-DJPEGXL_ENABLE_JPEGLI=OFF",
                    "-DJPEGXL_ENABLE_SKCMS=OFF",
                    "-DJPEGXL_VERSION=#{version}",
                    "-DJPEGXL_ENABLE_MANPAGES=ON",
                    "-DCMAKE_INSTALL_RPATH=#{rpath}",
                    "-DPython_EXECUTABLE=#{Formula["asciidoc"].libexec"binpython"}",
                    "-DPython3_EXECUTABLE=#{Formula["asciidoc"].libexec"binpython3"}",
                    *std_cmake_args
    system "cmake", "--build", "build"
    system "cmake", "--build", "build", "--target", "install"

    # Avoid rebuilding dependents that hard-code the prefix.
    inreplace (lib"pkgconfig").glob("*.pc"), prefix, opt_prefix
  end

  test do
    system bin"cjxl", test_fixtures("test.jpg"), "test.jxl"
    assert_path_exists testpath"test.jxl"

    (testpath"jxl_test.c").write <<~C
      #include <jxlencode.h>
      #include <stdlib.h>

      int main()
      {
          JxlEncoder* enc = JxlEncoderCreate(NULL);
          if (enc == NULL) {
            return EXIT_FAILURE;
          }
          JxlEncoderDestroy(enc);
          return EXIT_SUCCESS;
      }
    C
    jxl_flags = shell_output("pkgconf --cflags --libs libjxl").chomp.split
    system ENV.cc, "jxl_test.c", *jxl_flags, "-o", "jxl_test"
    system ".jxl_test"

    (testpath"jxl_threads_test.c").write <<~C
      #include <jxlthread_parallel_runner.h>
      #include <stdlib.h>

      int main()
      {
          void* runner = JxlThreadParallelRunnerCreate(NULL, 1);
          if (runner == NULL) {
            return EXIT_FAILURE;
          }
          JxlThreadParallelRunnerDestroy(runner);
          return EXIT_SUCCESS;
      }
    C
    jxl_threads_flags = shell_output("pkgconf --cflags --libs libjxl_threads").chomp.split
    system ENV.cc, "jxl_threads_test.c", *jxl_threads_flags, "-o", "jxl_threads_test"
    system ".jxl_threads_test"
  end
end