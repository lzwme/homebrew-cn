class SpirvHeaders < Formula
  desc "Headers for SPIR-V"
  homepage "https:github.comKhronosGroupSPIRV-Headers"
  url "https:github.comKhronosGroupSPIRV-Headersarchiverefstagsvulkan-sdk-1.3.275.0.tar.gz"
  sha256 "d46b261f1fbc5e85022cb2fada9a6facb5b0c9932b45007a77fe05639a605bd1"
  license "MIT"
  head "https:github.comKhronosGroupSPIRV-Headers.git", branch: "main"

  livecheck do
    url :stable
    regex(^(?:vulkan[._-])?sdk[._-]v?(\d+(?:\.\d+)+)$i)
  end

  bottle do
    sha256 cellar: :any_skip_relocation, all: "836978c6bed9aebc4e9b45c20d25a1b088c25d55d2ce411c1cd5bd5fd9d3e16e"
  end

  depends_on "cmake" => [:build, :test]

  def install
    system "cmake", "-S", ".", "-B", "build", *std_cmake_args
    system "cmake", "--build", "build"
    system "cmake", "--install", "build"

    pkgshare.install "tests"
  end

  test do
    cp pkgshare"testsexample.cpp", testpath

    (testpath"CMakeLists.txt").write <<~EOS
      add_library(SPIRV-Headers-example
                  ${CMAKE_CURRENT_SOURCE_DIR}example.cpp)
      target_include_directories(SPIRV-Headers-example
                  PRIVATE ${SPIRV-Headers_SOURCE_DIR}include)
    EOS

    system "cmake", "-S", ".", "-B", "build", *std_cmake_args
    system "cmake", "--build", "build"
  end
end