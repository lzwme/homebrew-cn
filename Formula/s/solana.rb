class Solana < Formula
  desc "Web-Scale Blockchain for decentralized apps and marketplaces"
  homepage "https://www.anza.xyz/"
  url "https://ghfast.top/https://github.com/anza-xyz/agave/archive/refs/tags/v4.0.2.tar.gz"
  sha256 "5e81209a02428c74621ace776ba36391897e85524fe5cd083b802bf8cff66115"
  license "Apache-2.0"
  version_scheme 1

  livecheck do
    url :stable
    strategy :github_latest
  end

  bottle do
    sha256 cellar: :any, arm64_tahoe:   "025d5fa5669836aba6b74d49104de1b291c17a8b1b5f4dc16eaf6d772c94592d"
    sha256 cellar: :any, arm64_sequoia: "d2684898924f2ae67f0ff2fa5961c3a2ba025ece813baaf6a8d0e35947046e6d"
    sha256 cellar: :any, arm64_sonoma:  "8e853654884c550b89096cadc6b22eddc416ce7806a8b120e862090017cf10a0"
    sha256 cellar: :any, sonoma:        "188dbfb2d32161c330376f0f88cffe7a124a5b4490dec2919d4ec0489be9ffb4"
    sha256 cellar: :any, arm64_linux:   "58323269024a9e49b6de2354711d3abd304e9f7590d0eb47db9ba9bae638fc68"
    sha256 cellar: :any, x86_64_linux:  "8a24914f4502f38bc9025e38f76e8f1a79c3cca5c5c92767b7b0810d0b078018"
  end

  depends_on "llvm" => :build # for libclang
  depends_on "pkgconf" => :build
  depends_on "protobuf" => :build
  depends_on "rust" => :build
  depends_on "openssl@3"
  depends_on "rocksdb"

  uses_from_macos "bzip2"

  # Work around Homebrew-specific issue using Apple Clang 1700 (LLVM 19) by updating cc-rs
  # https://github.com/Homebrew/brew/issues/21112
  patch :DATA

  def install
    # Work around librocksdb-sys build failure with Apple libclang, "Library not loaded: @rpath/libclang.dylib"
    ENV["LIBCLANG_PATH"] = Formula["llvm"].opt_lib.to_s if OS.mac?

    # Use brew dependencies
    ENV["PROTOC"] = Formula["protobuf"].opt_bin/"protoc"
    ENV["ROCKSDB_LIB_DIR"] = Formula["rocksdb"].opt_lib

    bins = %w[
      cli
      keygen
      stake-accounts
      tokens
      validator
      watchtower
    ]
    bins_dcou = %w[
      ledger-tool
    ]
    (bins + bins_dcou).each do |bin|
      system "cargo", "install", "--no-default-features", *std_cargo_args(path: bin)
    end

    generate_completions_from_executable(bin/"solana", "completion", shell_parameter_format: "--shell=",
                                                                     shells:                 [:bash, :zsh, :fish])
    # `:pwsh` string is "pwsh" in the shell_parameter_format,
    # so we need to write the completion manually since solana expects "powershell"
    (pwsh_completion/"solana").write Utils.safe_popen_read({ "SHELL" => "pwsh" }, bin/"solana", "completion",
"--shell=powershell")
  end

  test do
    output = shell_output("#{bin}/solana-keygen new --no-bip39-passphrase --no-outfile")
    assert_match "Generating a new keypair", output
    assert_match version.to_s, shell_output("#{bin}/solana-keygen --version")
  end
end

__END__
