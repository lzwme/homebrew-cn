class Groovy < Formula
  desc "Java-based scripting language"
  homepage "https://www.groovy-lang.org/"
  url "https://groovy.jfrog.io/artifactory/dist-release-local/groovy-zips/apache-groovy-binary-4.0.28.zip"
  sha256 "6a052bfd2ca77d57e0db304a313dd9a729945c5a31420e6d48505be4840bfc54"
  license "Apache-2.0"

  livecheck do
    url "https://groovy.jfrog.io/artifactory/dist-release-local/groovy-zips/"
    regex(/href=.*?apache-groovy-binary[._-]v?(\d+(?:\.\d+)+)\.zip/i)
  end

  bottle do
    sha256 cellar: :any_skip_relocation, arm64_sequoia: "ebadc692a86804a9d1784c7486df27d1fc809f87cd223fd3f036addda36dfd77"
    sha256 cellar: :any_skip_relocation, arm64_sonoma:  "74788fe54238ec1dad3d079f36b22770ff38ab781993f7e0ecd7e81c5dad43ca"
    sha256 cellar: :any_skip_relocation, arm64_ventura: "17dc84ab57f2d78ce2c09afd36566a50001677c55867f064dff9d824582b68a3"
    sha256 cellar: :any_skip_relocation, sonoma:        "2b2441637a64ddfc8ef107fe8d96051a873f967062985fa62499730af4bda30c"
    sha256 cellar: :any_skip_relocation, ventura:       "a33922e5ed37110b15d16e6d65e3ea642c6a9e8b084ad3ab1edaef5653a2145f"
    sha256 cellar: :any_skip_relocation, arm64_linux:   "660cc27f3cb0f27a93dd4fce308dd3df609bf3cb0c0de1c0ba168968cd707f2d"
    sha256 cellar: :any_skip_relocation, x86_64_linux:  "660cc27f3cb0f27a93dd4fce308dd3df609bf3cb0c0de1c0ba168968cd707f2d"
  end

  depends_on "openjdk"

  on_macos do
    # Temporary build dependencies for compiling jansi-native
    depends_on "autoconf" => :build
    depends_on "automake" => :build
    depends_on "libtool" => :build
    depends_on "maven" => :build

    # jansi-native is used to build native binary to support Apple Silicon.
    # Source version is from jline-2.14.6 -> jansi-1.12 -> jansi-native-1.6
    # TODO: Remove once updated to jline-3.x: https://issues.apache.org/jira/browse/GROOVY-8162
    resource "jansi-native" do
      url "https://ghfast.top/https://github.com/fusesource/jansi-native/archive/refs/tags/jansi-native-1.6.tar.gz"
      sha256 "f4075ad012c9ed79eaa8d3240d869e10d94ca8b130f3e7dac2ba3978dce0fb21"

      # Update pom.xml to replace unsupported Java 6 source and to disable universal binary
      patch :DATA
    end
  end

  conflicts_with "groovysdk", because: "both install the same binaries"

  def install
    if OS.mac?
      jline_jar = buildpath/"lib/jline-2.14.6.jar"
      resource("jansi-native").stage do
        # Fix compile with newer Clang
        ENV.append_to_cflags "-Wno-implicit-function-declaration" if DevelopmentTools.clang_build_version >= 1403

        system "mvn", "-Dplatform=osx", "prepare-package"
        system "zip", "-d", jline_jar, "META-INF/native/*"
        system "jar", "-uvf", jline_jar,
                      "-C", "target/generated-sources/hawtjni/lib",
                      "META-INF/native/osx64/libjansi.jnilib"
      end
    end

    # Don't need Windows files.
    rm(Dir["bin/*.bat"])

    libexec.install "bin", "conf", "lib"
    bin.install Dir["#{libexec}/bin/*"] - ["#{libexec}/bin/groovy.ico"]
    bin.env_script_all_files libexec/"bin", Language::Java.overridable_java_home_env
  end

  def caveats
    <<~EOS
      You should set GROOVY_HOME:
        export GROOVY_HOME=#{opt_libexec}
    EOS
  end

  test do
    output = shell_output("#{bin}/grape install org.activiti activiti-engine 5.16.4")
    assert_match "found org.activiti#activiti-engine;5.16.4", output
    assert_match "65536\n===> null\n", pipe_output("#{bin}/groovysh", "println 64*1024\n:exit\n")
  end
end

__END__
