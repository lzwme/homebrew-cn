class GoLibrespot < Formula
  desc "Spotify client"
  homepage "https://github.com/devgianlu/go-librespot"
  url "https://ghfast.top/https://github.com/devgianlu/go-librespot/archive/refs/tags/v0.7.2.tar.gz"
  sha256 "0ef75364250598ed1d1865fcb82eea5ceb890e710833ded70fcc9c4bc2365461"
  license "GPL-3.0-only"
  head "https://github.com/devgianlu/go-librespot.git", branch: "master"

  bottle do
    sha256 cellar: :any,                 arm64_tahoe:   "cab275afe980170aa6b2f13cae2640ead1d4b91be8cc0a0b935e9c6b610899df"
    sha256 cellar: :any,                 arm64_sequoia: "7694893cf91dd2431e21513c44df4c1d8562d6bd77535e2277add3653a2d6700"
    sha256 cellar: :any,                 arm64_sonoma:  "a566377694ea533199b0424e254e9cf3cf55ead7e4e1e9614988ced9604137c5"
    sha256 cellar: :any,                 sonoma:        "73f2c09c4e3e89ccc3df0298ef4f37a4aa2c2db0e47f35d3d90c95095dc11d3b"
    sha256 cellar: :any_skip_relocation, arm64_linux:   "20a888ea99e709743fceeaddcf178757846d4b29203b268407459f9929f81f34"
    sha256 cellar: :any_skip_relocation, x86_64_linux:  "ed52791b9c1a5d1b7e6af843f5f2d2c9e7cd83a370f1dd8644f3d8aac5762481"
  end

  depends_on "go" => :build
  depends_on "pkgconf" => :build
  depends_on "flac"
  depends_on "libogg"
  depends_on "libvorbis"

  on_linux do
    depends_on "alsa-lib"
  end

  def install
    ENV["CGO_ENABLED"] = "1" if OS.linux? && Hardware::CPU.arm?

    ldflags = %W[
      -s -w
      -X github.com/devgianlu/go-librespot.version=#{version}
    ]
    ldflags << "-X github.com/devgianlu/go-librespot.commit=#{Utils.git_short_head(length: 8)}" if build.head?

    system "go", "build", *std_go_args(output: bin/"go-librespot", ldflags:), "./cmd/daemon"
    (var/"log").mkpath

    # On macOS, create a minimal config that selects the correct backend.
    return unless OS.mac?

    (buildpath/"config.yml").write <<~YAML
      # Auto-generated by Homebrew. You can edit this file.
      # Use CoreAudio via AudioToolbox on macOS.
      audio_backend: audio-toolbox
    YAML
    pkgetc.install "config.yml"
  end

  service do
    run [opt_bin/"go-librespot", "--config_dir", etc/"go-librespot"]
    keep_alive true
    log_path var/"log/go-librespot.log"
    error_log_path var/"log/go-librespot.log"
  end

  test do
    cfg = testpath/"config.yml"
    cfg.write <<~YAML
      volume_steps: not_a_number
    YAML

    output = shell_output("#{bin}/go-librespot --config_dir #{testpath} 2>&1", 1)
    assert_match "failed loading config", output
    assert_match "failed to unmarshal configuration", output
  end
end