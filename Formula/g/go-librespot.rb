class GoLibrespot < Formula
  desc "Spotify client"
  homepage "https://github.com/devgianlu/go-librespot"
  url "https://ghfast.top/https://github.com/devgianlu/go-librespot/archive/refs/tags/v0.3.3.tar.gz"
  sha256 "15678b4d6954b381fb9c5a84aca2b0d7706e4d0b2c3644bafcb79cea6d31064c"
  license "GPL-3.0-only"
  head "https://github.com/devgianlu/go-librespot.git", branch: "master"

  bottle do
    sha256 cellar: :any,                 arm64_sequoia: "83579e3caa527e18316188069045476c5976008ac15ae9ea16c4c8a70b47906c"
    sha256 cellar: :any,                 arm64_sonoma:  "6e9b1274855e8d421367fdb84a918ac7235c107f5b107fcc529e538e2dbc60f7"
    sha256 cellar: :any,                 arm64_ventura: "48360bf34f1fa7396224cd8301991d8dd95067c95d29ab1d2e4d4683103e7140"
    sha256 cellar: :any,                 sonoma:        "cc0e07fd00c3638e397ff8988f2aa287a1a688ef48648119d6d73a36945df4a9"
    sha256 cellar: :any,                 ventura:       "c69cdd6d48c67941d4ef22225b7c755e53c772dc81bcab61ce4593b5211475a7"
    sha256 cellar: :any_skip_relocation, arm64_linux:   "adcb0aa698023ec878b814c8829eed96dea090b647d0324f9864ad8105c46728"
    sha256 cellar: :any_skip_relocation, x86_64_linux:  "41a8f04961a86a633373480afe82f53debbe61d891a8171af57fd587dff441c7"
  end

  depends_on "go" => :build
  depends_on "pkgconf" => :build
  depends_on "libogg"
  depends_on "libvorbis"

  on_linux do
    depends_on "alsa-lib"
  end

  def install
    ldflags = %W[
      -s -w
      -X github.com/devgianlu/go-librespot.version=#{version}
    ]
    ldflags << "-X github.com/devgianlu/go-librespot.commit=#{Utils.git_short_head(length: 8)}" if build.head?

    system "go", "build", *std_go_args(output: bin/"go-librespot", ldflags: ldflags), "./cmd/daemon"

    # On macOS, create a minimal config that selects the correct backend.
    return unless OS.mac?

    (buildpath/"config.yml").write <<~YAML
      # Auto-generated by Homebrew. You can edit this file.
      # Use CoreAudio via AudioToolbox on macOS.
      audio_backend: audio-toolbox
    YAML
    pkgetc.install "config.yml"
  end

  def post_install
    (var/"log").mkpath
  end

  service do
    run [opt_bin/"go-librespot", "--config_dir", etc/"go-librespot"]
    keep_alive true
    log_path var/"log/go-librespot.log"
    error_log_path var/"log/go-librespot.log"
  end

  test do
    cfg = testpath/"config.yml"
    cfg.write <<~YAML
      volume_steps: not_a_number
    YAML

    output = shell_output("#{bin}/go-librespot --config_dir #{testpath} 2>&1", 1)
    assert_match "failed loading config", output
    assert_match "failed to unmarshal configuration", output
  end
end